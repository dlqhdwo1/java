package for_;

public class For01 {

	public static void main(String[] args) {
		int i=0;
		
		for(i =1; i<=10; i++) {
			System.out.println("Hello Java!!"+i);
		}
		
		System.out.println("탈출:"+i);

		
		for(i=10;i>=1;i--) {
			System.out.println(i);
		}
		
		System.out.println();
		
		for(i='A';i<='Z';i++) {
			System.out.println((char)i);
		}
		
		
	}

}
